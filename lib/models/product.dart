
class Product {
  final String id;
  final String name;
  final String description;
  final String material;
  final String dimensions;
  final String color;
  final String condition;
  final double price;
  final String image;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.material,
    required this.dimensions,
    required this.color,
    required this.condition,
    required this.price,
    required this.image,
  });
}
